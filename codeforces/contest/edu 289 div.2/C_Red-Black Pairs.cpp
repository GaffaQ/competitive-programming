ll fpb(ll a, ll b){
    if(b == 0) return a;
    return fpb(b, a % b);
}

ll kpk(ll a, ll b){
    return a / fpb(a,b) * b;
}

void solve(){
    
    ll n; cin >> n;
    string a,b; cin >> a >> b;

    ll dp[n+5];
    dp[0] = (a[0] != b[0]);
    for (int i = 1; i < n; i++) {
        //vertikal
        dp[i] = dp[i-1]+(a[i]!=b[i]);
        //horizon
        ll c = (a[i]!=a[i-1])+(b[i]!=b[i-1]);
        if(i>=2){
            dp[i] = min(dp[i],dp[i-2]+c);
        }else{
            dp[i] = min(dp[i],c);
        }
    }

    cout << dp[n-1] << endl;

}

